      <p><strong>Akokoľvek by bol DJ tým najlepším</strong> a najskúsenejším
        človekom, keď nemá dostatočnú kolekciu hudby, neuspeje. Široký výber zo
        všetkých žánrov (rock ´n´ roll, pop, swing, R&amp;B, klasika, ľudové
        piesne, Repete skladby, nové hity súčasnosti) sú silnou zbraňou. Svadobnej
        hostiny sa poväčšine zúčastnia takmer všetky vekové kategórie, od vašej
        starej mamy, tancuchtivého strýka, sesterníc a bratrancov, pojašených
        detí, dojatých rodičov a zabávajúcich sa súrodencov. Žánrom sa medze
        nekladú. Spravte si chvíľku čas a prekonzultuje i zostavenie playlistu
        (hraných skladieb). Spoločne s DJ vytvorte scenár, ozvučenie i osvetlenie
        podľa vašich predstáv. Dôsledný DJ nezabudne ani na technickú
        predprípravu s personálom, ktorý pomáha pri príprave a pri priebehu
        svadobnej hostiny.</p>


      <p><strong>A ako prebieha po hudobnej stránke svadobná hostina?</strong><br>
        „Keď vchádza pár do svadobnej sály, znie jemná klavírna hudba, ako
        podfarbenie. Pokiaľ majú predslov zamestnanci hotela, hrá jemná nerušivá
        inštrumentálna hudba. Nesmie zostať ticho, to by bolo nevhodné. Aj počas
        večere volím klavírnu hudbu v kombinácii so saxofónom, alebo španielskou
        gitarou, či akordeónom. Najdôležitejšia skladba pre novomanželov je po
        večeri tá, na ktorú budú tancovať svoj novomanželský tanec. Pokiaľ si
        pár nevybral konkrétnu skladbu, odporučím im valčík alebo "slaďák“.
        Následne po odohraní úvodnej skladby vyzvem na parket svadobných rodičov,
        pri ďalšom valčíku sú už na parkete svadobní hostia a rozbieha sa prvé
        tanečné kolo. Potom volím skladby ráznejšieho tempa, ale nie príliš
        rýchle, keďže len pred chvíľou sa dokonzumovala večera. Po prestávke,
        ktorá by nemala byť veľmi dlhá, aby hostia „nevychladli“, sa tempo
        postupne zrýchľuje. Obľúbené je striedanie štýlov – slovenského a
        zahraničného oldies. Čím bližšie je ráno, tým je repertoár
        súčasnejší," popisuje zábavu DJ Benny. Zvyčajne sa svadobná zábava
        končí o 4. hodine rannej, ak si nevesta so ženíchom neobjednajú
        nelimitovaný čas zábavy, ktorý je, samozrejme, odzrkadlený v cene za
        kompletné služby.</p>

      <p><strong>Pokiaľ chcete mať</strong> svoju svadbu v deň „D“ bez
        stresov, je nutné prebrať s DJ-om podrobne všetko vopred. Budete si istí
        každým krokom a pripravení na rôzne situácie. Posledným, no zato
        najdôležitejším bodom plánovania, je písomná zmluva s DJ-om, v ktorej
        sú presne stanovené podmienky. A nič vám už nestojí v ceste pretancovať
        s vašimi hosťami celú svadobnú noc.</p>

