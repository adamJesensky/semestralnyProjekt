      <strong>A čo by ste mali o svojich kandidátoch nepochybne
        vedieť?</strong><br>
      <p>
        Opýtajte sa ich, koľko svadieb odohrajú ročne, aké majú technické
        vybavenie a v akej cenovej relácii svoje služby ponúkajú. Majú v čase
        vašej hostiny naplánované aj iné akcie? A čo program a prestávky? Buďte
        na všetko pripravení. Nespoliehajte sa len na zapísané meno a telefónne
        číslo vo vašom diári. Pri osobnom stretnutí sami vytušíte, či je pre
        vás ten „pravý“. Nebojte sa telefonicky komunikovať s vybratým adeptom.
        Pozor! Mail nie je vhodný komunikačný prostriedok na zistenie množstva
        informácií. Snažte sa zistiť čo najviac, aby ste predišli prípadným
        nedorozumeniam. Čo urobí váš DJ v prípade, ak ochorie alebo mu zlyhá
        technika? Vie pracovať aj s hovoreným slovom?</p>

      <p>„<strong>Suma za hudobnú produkciu</strong> sa vypočítava podľa
        technickej náročnosti a za môj osobný výkon. Vo finálnej vykalkulovanej
        cene je honorár DJ-a, odmena technikovi a vodičovi v jednej osobe, použitie
        zvukovej, svetelnej aparatúry a efektov, doprava, montáž a demontáž
        techniky. Štandardne ponúkam 60 000 skladieb v archíve hudby,
        niekoľkonásobné telefonické konzultácie a zostavenie scenára a
        predprípravu chodu celej svadby, mixovanie hudby, moderovanie akcie
        sprievodným slovom. V prípade záujmu aj súťaže a hry pre svadobčanov,“
        predstavuje svoje služby DJ Benny.<br>
        Najviac DJ-ov uprednostňuje pevnú taxu za niekoľko hodín. Nadrámec
        dohodnutých hodín sa pripláca. A, samozrejme, aj pri využití špeciálnych
        efektov. Nízke ceny sú mnohokrát veľkým lákadlom, no môžete sa pekne
        popáliť. V tomto prípade si DJ-i účtujú viac za dopravu, osvetlenie,
        techniku či moderovanie. Takže si dávajte pozor.</p>

