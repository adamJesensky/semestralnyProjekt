      <p><strong>Vybrať si svadobného DJ-a</strong> je ťažkou skúškou
        predsvadobnej prípravy, ale nebojte sa. Veď všetci vaši hostia neovládajú
        tanec na profesionálnej úrovni, ale ide im o poriadnu zábavu, nevešajte
        preto zbytočne hlavu.</p>

      <p>Ale pekne po poriadku. Výber DJ-a netreba podceniť. Nestačí len
        „kliknúť“ gombík a zábava je na svete. Preto postupujte zodpovedne a
        nevpustite k DJ pultu pregélovaného chlapca v rozopnutej košeli, so
        slnečnými okuliarmi, ktorý bude nečinne stáť a púšťať jednu
        „odrhovačku“ za druhou. Ak teda nechcete byť na dedinskej
        diskotéke…</p>

      <p><strong>Rady od priateľov a známych sú správnou voľbou.</strong>
        Prípadne sa porozprávajte s niekým, kto už má svadbu za sebou. Jeho rady
        budú pre vás nad zlato, lebo najcennejšie sú osobné skúsenosti. Kontakty
        na DJ-ov sa dajú nájsť aj na internete. Vyhľadajte si vždy však viacerých
        a preverte ich rozličnými otázkami. Nech vás nenachytajú násťroční
        tínedžeri bez väčších skúseností, alebo agentúry, ktoré nie vždy
        konajú v záujme klienta a často ani osobne nepoznajú odporúčaného DJ-a.
        Na pamäti majte zodpovednosť a profesionalitu ponúkaných služieb.
        „V dnešnej dobe sa už nedá zladiť dobrý DJ a lacný DJ. Preto by sa
        snúbenci nemali dať zlákať nízkou cenou. Či je svadba veľká alebo malá,
        je to úplne jedno. Musím odviesť kvalitný výkon aj pre zopár
        jednotlivcov,“ priznáva profesionálny DJ Benny, ktorý „hral“ na
        rôznych významných slovenských svadbách.</p>

